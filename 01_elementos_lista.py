lista = [32, 'hola', (1,2,3), ['rojo', 'rosa', 'negro']]

# Lista anidada
print(lista[-1])

# Elementos de la lista anidada
print(lista[-1][0])
print(lista[-1][1])
print(lista[-1][2])

# Elementos de la cadena 'rosa'
print(lista[-1][1][-4])
print(lista[-1][1][-3])
print(lista[-1][1][-2])
print(lista[-1][1][-1])
